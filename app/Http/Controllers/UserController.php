     */
    public function update(UpdateRequest $request, $id)
    {
        $user = User::find($id);
        $data = $request->validated();

        if($data['password'] == null){
            unset($data['password']);

        } else{

            $data['password'] =Hash::make($request->password);
        }

        $user->update($data);
        return redirect()->route('user.index');
    }

